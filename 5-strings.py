course = "Python Programming"
len(course)  # returns number of characters in list/ string
print(len(course))
print(course[0])  # prints first character
print(course[-1])  # prints last character

print(course[0:2])  # string slicing
print(course[10:])  # start
print(course[:10])  # stop
print(course[::2])  # skip
